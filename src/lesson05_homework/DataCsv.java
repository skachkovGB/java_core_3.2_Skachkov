package lesson05_homework;

public class DataCsv {

    private final Integer valueOne;
    private final Integer valueTwo;
    private final Integer valueThree;

    public DataCsv(Integer valueOne, Integer valueTwo, Integer valueThree){
        this.valueOne = valueOne;
        this.valueTwo = valueTwo;
        this.valueThree = valueThree;
    }

    public Integer getValueOne() {
        return valueOne;
    }

    public Integer getValueTwo() {
        return valueTwo;
    }

    public Integer getValueThree() {
        return valueThree;
    }
}
